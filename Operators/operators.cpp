#include<bits/stdc++.h>
using namespace std;

/*

AND- &
OR-  |
NOT- ~
XOR- ^


*/
int main(){

    int num1 = 71;
    int num2 = 62;

    // AND OPERATOR;


    std::cout<<"The AND of given nums is:"<< (num1 & num2)<<endl;
    std::cout<<"The OR of given nums is:"<< (num1 | num2)<<endl;
    std::cout<<"NOT of given nums is:"<< (~num1)<<endl;
    std::cout<<"XOR of given nums is:"<< (num1^num2)<<endl;



    return 0;
}


